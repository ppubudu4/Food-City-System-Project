    private void ShowThis(){
        Item ItemObj = new Item();
        switch (ItemObj.CShowInAdd()) {
            case 1:
                Cat CatObj = new Cat();
                CatObj.setSize(1084,590);
                CatObj.removeAll();
                Add_LPane.removeAll();
                Add_LPane.add(CatObj);
                Add_LPane.moveToFront(CatObj);
                Add_LPane.setVisible(true);
                CatObj.Cat_Btn_Back.setVisible(false);
                CatObj.Cat_Btn_Ok.setVisible(false);
                break;
            case 2:
                SubCat SubCatObj = new SubCat();
                SubCatObj.setSize(1084,590);
                SubCatObj.removeAll();
                Add_LPane.removeAll();
                Add_LPane.add(SubCatObj);
                Add_LPane.moveToFront(SubCatObj);
                Add_LPane.setVisible(true);
                SubCatObj.SubCat_Btn_Back.setVisible(false);
                SubCatObj.SubCat_Btn_Ok.setVisible(false);
                break;
            default:
                Sup SupObj = new Sup();
                if(SupObj.CShowInAdd()==3){
                    ContPers ContPersObj = new ContPers();
                    ContPersObj.setSize(1084,590);
                    ContPersObj.removeAll();
                    Add_LPane.removeAll();
                    Add_LPane.add(ContPersObj);
                    Add_LPane.moveToFront(ContPersObj);
                    Add_LPane.setVisible(true);
                    ContPersObj.ContPers_Btn_Back.setVisible(false);
                    ContPersObj.ContPers_Btn_Ok.setVisible(false);
                }   
                break;
        }
    }

