    private void AEmpSalAdv_Get_Btn_DoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AEmpSalAdv_Get_Btn_DoneActionPerformed
        if (AEmpSalAdv_Get_TextF_EmpID.getText().equals("") || AEmpSalAdv_Get_TextF_EmpID.getText() == null) {
            JOptionPane.showMessageDialog(this, "Plese enter the employee ID", "Error", 0);
            AEmpSalAdv_Get_TextF_EmpID.grabFocus();
        } else if (AEmpSalAdv_Get_TextF_SalAdvAmount.getText().equals("") || AEmpSalAdv_Get_TextF_SalAdvAmount.getText() == null) {
            JOptionPane.showMessageDialog(this, "Plese enter the salary advance amount", "Error", 0);
            AEmpSalAdv_Get_TextF_SalAdvAmount.grabFocus();
        } else if (AEmpSalAdv_Get_TextA_Dicription.getText().equals("") || AEmpSalAdv_Get_TextA_Dicription.getText() == null) {
            JOptionPane.showMessageDialog(this, "Plese enter the reson", "Error", 0);
            AEmpSalAdv_Get_TextA_Dicription.grabFocus();
        } else if (AEmpSalAdv_Get_TextF_ApprovedBy.getText().equals("") || AEmpSalAdv_Get_TextF_ApprovedBy.getText() == null) {
            JOptionPane.showMessageDialog(this, "Plese enter the person who approved", "Error", 0);
            AEmpSalAdv_Get_TextF_ApprovedBy.grabFocus();
        } else {
            try {
                ResultSet resPosID = ConnectDB.DBConnection().createStatement().executeQuery("SELECT Emp_Pos_De_ID FROM employee WHERE Emp_ID='" + AEmpSalAdv_Get_TextF_EmpID.getText() + "' AND Emp_Staus='" + 1 + "'");
                if (resPosID.next()) {
                    ResultSet resGot = ConnectDB.DBConnection().createStatement().executeQuery("SELECT Emp_ID FROM emp_sal_lone WHERE Emp_ID='" + AEmpSalAdv_Get_TextF_EmpID.getText() + "' AND ( Emp_Sal_Lon_Year='" + Year + "' AND Emp_Sal_Lon_Month='" + Month + "')");
                    if (resGot.next()) {
                        JOptionPane.showMessageDialog(this, "This employee can't get another salary advance", "Error", 0);
                        Clear();
                        AEmpSalAdv_Get_TextF_EmpID.grabFocus();
                    } else {
                        if (resPosID.next()) {
                            ResultSet resSal = ConnectDB.DBConnection().createStatement().executeQuery("SELECT Emp_Pos_De_Salary FROM emp_pos_detail WHERE Emp_Pos_De_ID='" + resPosID.getString("Emp_Pos_De_ID") + "'");
                            if (resSal.next()) {
                                int sal = Integer.parseInt(resSal.getString("Emp_Pos_De_Salary")) - 3000;
                                if (Integer.parseInt(AEmpSalAdv_Get_TextF_SalAdvAmount.getText()) > sal) {
                                    JOptionPane.showMessageDialog(this, "This employee can't get salary advance than " + sal, "Error", 0);
                                    AEmpSalAdv_Get_TextF_SalAdvAmount.setText("");
                                    AEmpSalAdv_Get_TextF_SalAdvAmount.grabFocus();
                                } else if (Integer.parseInt(AEmpSalAdv_Get_TextF_SalAdvAmount.getText()) < sal) {
                                    SimpleDateFormat timeFormatter = new SimpleDateFormat("h:mm a");
                                    ConnectDB.DBConnection().createStatement().executeUpdate("INSERT INTO emp_sal_lone VALUES('" + AEmpSalAdv_Get_TextF_EmpID.getText() + "', '" + Year + "', '" + Month + "', '" + Day + "', '" + timeFormatter.format(Calendar.getInstance().getTime()) + "', '" + AEmpSalAdv_Get_TextA_Dicription.getText() + "', '" + AEmpSalAdv_Get_TextF_SalAdvAmount.getText() + "', '" + AEmpSalAdv_Get_TextF_ApprovedBy.getText() + "')");
                                    JOptionPane.showMessageDialog(this, "Record successfully added");
                                    Clear();
                                    AEmpSalAdv_Get_TextF_EmpID.grabFocus();
                                }
                            }
                        }
                    }
                }else{
                    JOptionPane.showMessageDialog(this, "Enter a valid employee ID", "Error", 0);
                    AEmpSalAdv_Get_TextF_EmpID.setText("");
                    AEmpSalAdv_Get_TextF_EmpID.grabFocus();
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_AEmpSalAdv_Get_Btn_DoneActionPerformed

